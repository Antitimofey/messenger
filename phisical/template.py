import serial
from typing import Optional, Callable


class COMPort:
    """
    Класс для работы с COM-портом на физическом уровне.
    
    Предоставляет интерфейс для:
    - открытия/закрытия порта,
    - установки параметров RS-232C,
    - байтовой передачи/приёма,
    - управления потоком данных в кольцевой топологии.
    
    Разработчик канального уровня создаёт два экземпляра:
    - left_port = COMPort('input')   # порт, принимающий данные от предыдущего узла
    - right_port = COMPort('output') # порт, отправляющий данные следующему узлу
    """
    
    def __init__(self, role: str):
        """
        Инициализация объекта COM-порта.
        
        Параметры:
        ----------
        role : str
            Роль порта в кольцевой топологии:
            - 'input'  - порт для приёма данных от предыдущего узла
            - 'output' - порт для отправки данных следующему узлу
            
        Выход:
        ------
        None
        
        Исключения:
        -----------
        ValueError
            Если role не является 'input' или 'output'
        """
        raise NotImplementedError(
            "Метод __init__ должен быть реализован разработчиком физического уровня. "
            "Ожидается: сохранение роли порта, инициализация внутренних атрибутов "
            "(например, serial_handle = None, is_open = False, callback = None)."
        )
    
    def open_port(self, port_name: str, baudrate: int = 9600, bytesize: int = 8,
                  parity: str = 'N', stopbits: int = 1, timeout: float = 1.0) -> bool:
        """
        Открытие COM-порта с заданными параметрами.
        
        Параметры:
        ----------
        port_name : str
            Имя COM-порта (например, 'COM1', 'COM2', '/dev/ttyS0')
        baudrate : int, default=9600
            Скорость передачи (бит/с)
        bytesize : int, default=8
            Количество бит данных (5, 6, 7 или 8)
        parity : str, default='N'
            Чётность: 'N' (нет), 'E' (чёт), 'O' (нечёт)
        stopbits : int, default=1
            Количество стоп-бит (1 или 2)
        timeout : float, default=1.0
            Таймаут чтения в секундах
            
        Выход:
        ------
        bool
            True - порт успешно открыт и готов к работе,
            False - ошибка при открытии порта
            
        Исключения:
        -----------
        serial.SerialException
            При проблемах с доступом к порту (занят, не существует)
        ValueError
            При недопустимых значениях параметров
        """
        raise NotImplementedError(
            "Метод open_port должен быть реализован разработчиком физического уровня. "
            "Ожидается: вызов serial.Serial(), сохранение дескриптора, "
            "установка is_open = True, возврат True/False."
        )
    
    def close_port(self) -> bool:
        """
        Закрытие COM-порта и освобождение ресурсов.
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        bool
            True - порт успешно закрыт,
            False - порт уже был закрыт или ошибка закрытия
            
        Примечание:
        -----------
        После закрытия порта все операции чтения/записи должны возвращать ошибку.
        """
        raise NotImplementedError(
            "Метод close_port должен быть реализован разработчиком физического уровня. "
            "Ожидается: закрытие serial-соединения, сброс is_open = False, возврат True/False."
        )
    
    def set_parameters(self, baudrate: int = None, bytesize: int = None,
                       parity: str = None, stopbits: int = None,
                       timeout: float = None) -> bool:
        """
        Изменение параметров COM-порта без переоткрытия.
        
        Параметры:
        ----------
        baudrate : int, optional
            Новая скорость передачи (если None - оставить текущую)
        bytesize : int, optional
            Новое количество бит данных
        parity : str, optional
            Новая чётность ('N', 'E', 'O')
        stopbits : int, optional
            Новое количество стоп-бит
        timeout : float, optional
            Новый таймаут чтения
            
        Выход:
        ------
        bool
            True - параметры успешно изменены,
            False - ошибка изменения параметров (порт закрыт или параметры недопустимы)
            
        Исключения:
        -----------
        serial.SerialException
            При ошибках применения параметров к открытому порту
        """
        raise NotImplementedError(
            "Метод set_parameters должен быть реализован разработчиком физического уровня. "
            "Ожидается: применение новых параметров через serial.Serial.apply_settings() "
            "или переоткрытие порта."
        )
    
    def send_bytes(self, data: bytes) -> int:
        """
        Отправка байтов через COM-порт.
        
        Параметры:
        ----------
        data : bytes
            Байтовый буфер для отправки (не должен быть пустым)
            
        Выход:
        ------
        int
            Количество реально отправленных байт (должно совпадать с len(data) при успехе)
            
        Исключения:
        -----------
        serial.SerialTimeoutException
            При таймауте записи (если включена блокировка)
        serial.SerialException
            При ошибке записи (порт закрыт, аппаратная ошибка)
        ValueError
            Если data пуста или None
            
        Примечание:
        -----------
        Метод должен быть блокирующим до отправки всех байт или таймаута.
        """
        raise NotImplementedError(
            "Метод send_bytes должен быть реализован разработчиком физического уровня. "
            "Ожидается: вызов serial.Serial.write(data), обработка таймаутов, "
            "возврат количества отправленных байт."
        )
    
    def receive_bytes(self, num_bytes: int) -> Optional[bytes]:
        """
        Приём байтов из COM-порта (блокирующий вызов до таймаута).
        
        Параметры:
        ----------
        num_bytes : int
            Ожидаемое количество байт для чтения (1..1024)
            
        Выход:
        ------
        Optional[bytes]
            - bytes : принятые данные (длина может быть меньше num_bytes по таймауту)
            - None : ошибка чтения (порт закрыт, таймаут без данных)
            
        Исключения:
        -----------
        serial.SerialException
            При критической ошибке порта
        ValueError
            Если num_bytes <= 0 или > максимального буфера
            
        Примечание:
        -----------
        Метод ожидает данные в течение установленного таймаута (timeout).
        """
        raise NotImplementedError(
            "Метод receive_bytes должен быть реализован разработчиком физического уровня. "
            "Ожидается: вызов serial.Serial.read(num_bytes) с учётом таймаута, "
            "возврат прочитанных байт или None при ошибке."
        )
    
    def set_receive_callback(self, callback: Callable[[bytes], None]) -> None:
        """
        Установка асинхронного callback-а на получение данных.
        
        Параметры:
        ----------
        callback : Callable[[bytes], None]
            Функция, вызываемая при поступлении новых данных в порт.
            Принимает один аргумент - принятые байты (bytes).
            
        Выход:
        ------
        None
        
        Примечание:
        -----------
        Используется разработчиком канального уровня для организации
        асинхронного приёма (например, в отдельном потоке).
        """
        raise NotImplementedError(
            "Метод set_receive_callback должен быть реализован разработчиком физического уровня. "
            "Ожидается: сохранение callback-функции, запуск фонового потока чтения, "
            "вызов callback при поступлении данных."
        )
    
    def remove_receive_callback(self) -> None:
        """
        Удаление установленного callback-а и остановка фонового чтения.
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        None
        """
        raise NotImplementedError(
            "Метод remove_receive_callback должен быть реализован разработчиком физического уровня. "
            "Ожидается: остановка потока чтения, удаление callback-функции."
        )
    
    def is_open(self) -> bool:
        """
        Проверка состояния порта (открыт/закрыт).
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        bool
            True - порт открыт и готов к работе,
            False - порт закрыт или не инициализирован
        """
        raise NotImplementedError(
            "Метод is_open должен быть реализован разработчиком физического уровня. "
            "Ожидается: возврат внутреннего флага состояния порта."
        )
    
    def clear_buffers(self) -> bool:
        """
        Очистка входного и выходного буферов COM-порта.
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        bool
            True - буферы успешно очищены,
            False - ошибка очистки (порт закрыт)
            
        Примечание:
        -----------
        Используется при разрыве соединения или сбросе состояния.
        """
        raise NotImplementedError(
            "Метод clear_buffers должен быть реализован разработчиком физического уровня. "
            "Ожидается: вызов serial.Serial.flush() и serial.Serial.reset_input_buffer(), "
            "serial.Serial.reset_output_buffer()."
        )
    
    def get_role(self) -> str:
        """
        Получение роли порта ('input' или 'output').
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        str
            'input' или 'output' в зависимости от роли порта
        """
        raise NotImplementedError(
            "Метод get_role должен быть реализован разработчиком физического уровня. "
            "Ожидается: возврат сохранённого при __init__ значения role."
        )
    
    def get_port_name(self) -> Optional[str]:
        """
        Получение имени открытого COM-порта.
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        Optional[str]
            - str : имя открытого порта (например, 'COM1')
            - None : порт не открыт или не инициализирован
        """
        raise NotImplementedError(
            "Метод get_port_name должен быть реализован разработчиком физического уровня. "
            "Ожидается: возврат текущего имени порта или None."
        )
    
    def get_current_parameters(self) -> dict:
        """
        Получение текущих параметров COM-порта.
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        dict
            Словарь с параметрами:
            {
                'baudrate': int,
                'bytesize': int,
                'parity': str,
                'stopbits': int,
                'timeout': float,
                'port': str,
                'is_open': bool
            }
        """
        raise NotImplementedError(
            "Метод get_current_parameters должен быть реализован разработчиком физического уровня. "
            "Ожидается: сбор и возврат текущих параметров порта в виде словаря."
        )
    
    def wait_for_data(self, timeout: float = None) -> bool:
        """
        Ожидание поступления данных во входной буфер.
        
        Параметры:
        ----------
        timeout : float, optional
            Время ожидания в секундах (если None - использовать таймаут порта)
            
        Выход:
        ------
        bool
            True - данные доступны для чтения,
            False - таймаут истёк или порт закрыт
        """
        raise NotImplementedError(
            "Метод wait_for_data должен быть реализован разработчиком физического уровня. "
            "Ожидается: использование serial.Serial.in_waiting или select() для проверки наличия данных."
        )
    
    def get_bytes_available(self) -> int:
        """
        Получение количества байт, доступных для чтения во входном буфере.
        
        Параметры:
        ----------
        None
        
        Выход:
        ------
        int
            Количество байт в буфере (0 - нет данных, -1 - порт закрыт)
        """
        raise NotImplementedError(
            "Метод get_bytes_available должен быть реализован разработчиком физического уровня. "
            "Ожидается: возврат serial.Serial.in_waiting или 0 при ошибке."
        )